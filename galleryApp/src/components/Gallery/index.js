class Gallery extends Component {
  state = {
    thumbnail: imagesList[0].imageUrl,
    altKey: imagesList[0].imageAltText,
  }

  displayImage = (imageUrl, imageAltText) => {
    //   this.setState()
    // const {imageUrl} = this.props
    this.setState({thumbnail: imageUrl})
    this.setState({altKey: imageAltText})
  }

  render() {
    const {thumbnail, altKey} = this.state
    return (
      <div className="outer">
        <div className="inner">
          <img alt={altKey} className="thumbnail" src={thumbnail} />
          <h1 className="heading">Nature Photography</h1>
          <p className="para">Nature Photography by Rahul</p>
          <ul className="imageRow">
            {imagesList.map(each => (
              <ThumbnailItem
                key={each.thumbnailAltText}
                thumbnailDetails={each}
                displayImage={this.displayImage}
                isSelect={thumbnail === each.imageUrl}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
